#       Filename: for05.sh
#    Description: ping 172.16.X.Y内的所有主机；172.16.0-255.1-254,
#运行此代码需谨慎，中间根本停不下来，可以将里面的数字修改小点。
#****************************************************
for i in {0..255}
do
	for j in `seq 1 254`
	do
		ping -c 1 -w 1 172.16.${i}.${j}
	done
done

